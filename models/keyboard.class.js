/**
 * Represents the input state of the keyboard for controlling the game.
 * Each property corresponds to a key and indicates whether that key is currently pressed.
 * @class
 */
class Keyboard {
    /**
     * Indicates whether the left arrow or "A" key is pressed.
     * @type {boolean} left
     * @type {boolean} right
     * @type {boolean} up
     * @type {boolean} down
     * @type {boolean} space
     */
    LEFT = false;
    RIGHT = false;
    UP = false;
    DOWN = false;
    SPACE = false;
}